package com.hypersocket.auth;


public enum BrowserEnvironment {

	USER_AGENT("User-Agent"),
	HOST("Host"),
	AUTHORIZATION("Authorization");
	
	private final String val;
	
	private BrowserEnvironment(final String val) {
		this.val = val;
	}
	
	public String toString() {
		return val;
	}

}
